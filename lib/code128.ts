const START_B = 104;
const STOP = 106;

// Encode a string as a Code-128B module-width pattern (bar,space,bar,…).
export function code128b(input: string): number[] {
  const data = (input || "").replace(/[^\x20-\x7e]/g, "?");
  const codes: number[] = [START_B];
  for (const ch of data) codes.push(ch.charCodeAt(0) - 32);
  let sum = START_B;
  data.split("").forEach((ch, i) => {
    sum += (ch.charCodeAt(0) - 32) * (i + 1);
  });
  codes.push(sum % 103);
  codes.push(STOP);
  const pattern: number[] = [];
  for (const c of codes) pattern.push(...C128[c]);
  return pattern;
}
